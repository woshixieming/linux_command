export const getTopCommands = (count: number = 20): LinuxCommand[] => {
  return [...linuxCommands]
    .sort((a, b) => a.popularity - b.popularity)
    .slice(0, count);
};

export const getCommandsByCategory = (category: string): LinuxCommand[] => {
  return linuxCommands.filter(cmd => cmd.category === category);
};

export const getCommandById = (id: string): LinuxCommand | undefined => {
  return linuxCommands.find(cmd => cmd.id === id);
};

export const generateMarkdown = (commands: LinuxCommand[]): string => {
  let md = `# Linux 命令速查表\n\n`;
  md += `> 生成时间: ${new Date().toLocaleString('zh-CN')}\n\n`;
  md += `> 共收录 ${commands.length} 条命令\n\n`;
  md += `---\n\n`;

  commands.forEach(cmd => {
    md += `## ${cmd.name}\n\n`;
    md += `**描述**: ${cmd.description}\n\n`;
    md += `**类别**: ${cmd.category}\n\n`;
    md += `**语法**: \`${cmd.syntax}\`\n\n`;
    
    if (cmd.args.length > 0) {
      md += `### 参数\n\n`;
      md += `| 参数 | 说明 |\n`;
      md += `|------|------|\n`;
      cmd.args.forEach(arg => {
        md += `| \`${arg.name}\` | ${arg.description}`;
        if (arg.example) md += ` (${arg.example})`;
        md += ` |\n`;
      });
      md += `\n`;
    }

    if (cmd.examples.length > 0) {
      md += `### 示例\n\n`;
      cmd.examples.forEach(ex => {
        md += `- ${ex.description}\n`;
        md += `  \`\`\`bash\n`;
        md += `  ${ex.command}\n`;
        md += `  \`\`\`\n\n`;
      });
    }

    md += `---\n\n`;
  });

  md += `\n---\n\n`;
  md += `*本速查表由 Linux 命令学习站生成*\n`;

  return md;
};
